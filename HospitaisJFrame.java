    public void mostraLista(ArrayList<Hospital> lista){
        limpaListaVisual();
        Iterator<Hospital> it = lista.iterator();
        while(it.hasNext()){
            // total = it.next().leitosDisponiveis();--> Alternativa            
            Hospital h = it.next();
            insereHospitalLista(h);
        }       
    }
    
    public void limpaListaVisual(){
        listaVisual.clear();
    }
    
    public void insereHospitalLista(Hospital h){
        listaVisual.addElement(h);
    }
    
