    public Hospital obterHospital(){
        String nome       = nomeHospitaljTextField1.getText();
        int    totLeitos  = Integer.parseInt(leitosTotaisjTextField2.getText())  ;
        int    leitoOc    = Integer.parseInt(leitosOcupadojTextField3.getText());       
        Hospital h = new Hospital(nome, totLeitos, leitoOc);        
        return h;
    }
